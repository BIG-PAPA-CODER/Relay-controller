/* USER CODE BEGIN 0 */
float battery_measure(){
	HAL_ADC_Start(&hadc);
	if (HAL_ADC_PollForConversion(&hadc, 100) == HAL_OK) {
		adc_value = HAL_ADC_GetValue(&hadc);  // Read ADC value
	}
	HAL_ADC_Stop(&hadc);  // Stop ADC

    // Convert ADC reading to voltage
    v_adc = (adc_value * VREF) / ADC_MAX_VALUE;
    v_bat = v_adc * VOLTAGE_DIVIDER_RATIO;  // Convert to actual battery voltage

    return v_bat;
}
